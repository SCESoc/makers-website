<h1>Showcase Your Project</h1>
<p>
	This website is a way for you to showcase projects you work on to the Carleton Community. The goal
	of the Project Showcase section is to surface projects that are being worked on in the community.
	This is a great way to show off your work to the community and to get feedback from other makers.
	We hope that this website will act as a platform to discover projects, collaborate, and share
	experiences among the Carleton Community
</p>
<p>
	Anyone in Carleton University can add projects to this website. There are 3 ways you can add a
	project to this website:
</p>

<ol>
	<li>
		<b
			>By filling out the folowing form (Recommended if you are NOT familiar with GitHub or
			Markdown)</b
		>
		<br /> Fill out this form.
		<br />
		Makers Project Showcase Form -
		<SmartLink class="highlight-link" href="https://forms.gle/v7XRcfKK8TexfUBD7">
			https://forms.gle/v7XRcfKK8TexfUBD7
		</SmartLink>
	</li>
	<li>
		<b
			>By adding a Markdown file on GitHub (Recommended if you are familiar with GitHub and
			Markdown)</b
		>
		<br />
		Create a pull request to add a markdown file in this <SmartLink
			class="highlight-link"
			href="https://github.com/SCESoc/makers-website/tree/main/data/projects"
		>
			directory</SmartLink
		> using the following <SmartLink
			class="highlight-link"
			href="https://github.com/SCESoc/makers-website/blob/main/data/projects/template.md
	">template</SmartLink
		> to add information about your project. You may have to fork the repository to add the file. We
		will review it and merge it after making sure the format is valid.
	</li>
	<li>
		<b
			>By creating a Markdown file and emailing it (Recommended if you are familiar with Markdown
			but not GitHub)</b
		>
		<br />
		Create a Markdown file using the following <SmartLink
			class="highlight-link"
			href="https://github.com/SCESoc/makers-website/blob/main/data/projects/template.md
">template</SmartLink
		> and email it to <SmartLink class="highlight-link" href="academic@scesoc.ca">
			academic@scesoc.ca
		</SmartLink>
	</li>
</ol>
<p>
	Please reach out to <SmartLink class="highlight-link" href="academic@scesoc.ca">
		academic@scesoc.ca
	</SmartLink> if you have any questions.
</p>
<blockquote>
	<b>Note:</b> Don't worry about having the perfect information about your project right off the bat.
	You can always update the information at a later date.
